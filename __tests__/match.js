import sortCharacter from '../src/match';

const characters = [
  { name: 'мечник', health: 10 },
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
];

const charactersSort = [
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
  { name: 'мечник', health: 10 },
];

test('', () => {
  sortCharacter(characters);
  expect(charactersSort).toEqual(characters);
});
